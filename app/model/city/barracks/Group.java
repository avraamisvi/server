package model.city.barracks;

import java.util.List;

public class Group {
	
	List<Archer> archers; 
	List<Cannon> bazookas;
	List<Catapult> catapults;
	List<Doctor> doctors;
	List<Knight> knights;
	List<Rifleman> riflemans;
	List<SlingMan> slingMans;
	List<SpearMan> spearMans;
	List<SwordMan> swordMans;
	
	public List<Archer> getArchers() {
		return archers;
	}
	public void setArchers(List<Archer> archers) {
		this.archers = archers;
	}
	public List<Cannon> getBazookas() {
		return bazookas;
	}
	public void setBazookas(List<Cannon> bazookas) {
		this.bazookas = bazookas;
	}
	public List<Catapult> getCatapults() {
		return catapults;
	}
	public void setCatapults(List<Catapult> catapults) {
		this.catapults = catapults;
	}
	public List<Doctor> getDoctors() {
		return doctors;
	}
	public void setDoctors(List<Doctor> doctors) {
		this.doctors = doctors;
	}
	public List<Knight> getKnights() {
		return knights;
	}
	public void setKnights(List<Knight> knights) {
		this.knights = knights;
	}
	public List<Rifleman> getRiflemans() {
		return riflemans;
	}
	public void setRiflemans(List<Rifleman> riflemans) {
		this.riflemans = riflemans;
	}
	public List<SlingMan> getSlingMans() {
		return slingMans;
	}
	public void setSlingMans(List<SlingMan> slingMans) {
		this.slingMans = slingMans;
	}
	public List<SpearMan> getSpearMans() {
		return spearMans;
	}
	public void setSpearMans(List<SpearMan> spearMans) {
		this.spearMans = spearMans;
	}
	public List<SwordMan> getSwordMans() {
		return swordMans;
	}
	public void setSwordMans(List<SwordMan> swordMans) {
		this.swordMans = swordMans;
	}
}
